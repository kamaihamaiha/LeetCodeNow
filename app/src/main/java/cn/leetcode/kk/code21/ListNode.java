package cn.leetcode.kk.code21;

/**
 * 单链表
 */
public class ListNode {

     int value;
     ListNode nextNode;

    public ListNode() {
    }

    public ListNode(int value) {
        this.value = value;
    }

    public ListNode(int value, ListNode nextNode) {
        this.value = value;
        this.nextNode = nextNode;
    }


}
