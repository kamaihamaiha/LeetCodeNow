package cn.leetcode.kk.code21;

/**
 * 单链表
 */
class ListNode {

    int value;
    ListNode nextNode;

    ListNode() {
    }

    ListNode(int value) {
        this.value = value;
    }

    ListNode(int value, ListNode nextNode) {
        this.value = value;
        this.nextNode = nextNode;
    }


}
